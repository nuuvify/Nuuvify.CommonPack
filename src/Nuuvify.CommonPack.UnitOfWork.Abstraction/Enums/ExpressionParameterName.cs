namespace Nuuvify.CommonPack.UnitOfWork.Abstraction.Enums;

/// <summary>
/// Defines standard parameter names for LINQ expressions.
/// </summary>
public enum ExpressionParameterName
{
    /// <summary>
    /// Represents the "model" parameter name commonly used in LINQ expressions.
    /// </summary>
    Model,

    /// <summary>
    /// Represents the "entity" parameter name used in LINQ expressions.
    /// </summary>
    Entity,

    /// <summary>
    /// Represents the "item" parameter name used in LINQ expressions.
    /// </summary>
    Item,

    /// <summary>
    /// Represents the "p" parameter name used in LINQ expressions.
    /// </summary>
    P,

    /// <summary>
    /// Represents the "param" parameter name used in LINQ expressions.
    /// </summary>
    Param,

    /// <summary>
    /// Represents the "x" parameter name used in LINQ expressions.
    /// </summary>
    X
}